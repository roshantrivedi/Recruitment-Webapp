package dao;


public class ApplyDao {
    
    private int id;
    private String field;
    private String link;
    
    
    public int getId(){
        return id;
    }
    public void setId(int id)
    {
        this.id=id;
    }
    public String getField(){
        return field;
    }
    public void setField(String field)
    {
        this.field=field;
    }
    public String getLink(){
        return link;
    }
    public void setLink(String link)
    {
        this.link=link;
    }
    
    
    
}
